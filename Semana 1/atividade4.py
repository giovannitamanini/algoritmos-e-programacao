# Algoritmo para o cálculo da área de um terreno retangular

# Entradas
largura_do_terreno = float(input("Qual é a largura do terreno em metros? "))
profundidade_do_terreno = float(input("Qual é a profundidade do terreno em metros? "))

# Cálculo
area_do_terreno = largura_do_terreno * profundidade_do_terreno

# Saída
print("A área do terreno é de",area_do_terreno,"m²")